    private void tambahBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahBtnActionPerformed
        try {
            // TODO add your handling code here:
            dor.insert();
        }
        catch (SQLException ex) {
            Logger.getLogger(ViewOrderan.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (Exception ex) {
            if(ex instanceof CustomException){
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            else if(ex instanceof NumberFormatException){
                JOptionPane.showMessageDialog(this, "Input Banyak harus berupa angka!");
            }
            else{
                JOptionPane.showMessageDialog(this, ex);
            }
        }
        finally {
            dor.isitabel();
        }
    }//GEN-LAST:event_tambahBtnActionPerformed

    private void hapusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBtnActionPerformed
        try {
            // TODO add your handling code here:
            dor.delete();
        } catch (CustomException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        finally {
            dor.isitabel();
            idLabel.setText(" ");
            jComboBoxItem.setSelectedIndex(0);
            banyakTf.setText(null);
            totalLabel.setText(" ");
            pemesanTa.setText(null);
            tambahBtn.setEnabled(true);
            jComboBoxItem.setEnabled(true);
            banyakTf.setEditable(true);
            pemesanTa.setEditable(true);
        }
    }//GEN-LAST:event_hapusBtnActionPerformed

