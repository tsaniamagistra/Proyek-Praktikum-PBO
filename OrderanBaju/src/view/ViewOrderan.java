    private void tabelOrderanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelOrderanMouseClicked
        // TODO add your handling code here:
        int baris = tabelOrderan.getSelectedRow();
        idLabel.setText(tabelOrderan.getValueAt(baris , 0).toString());
        jComboBoxItem.setSelectedItem(tabelOrderan.getValueAt(baris, 1).toString());
        banyakTf.setText(tabelOrderan.getValueAt(baris , 2).toString());
        totalLabel.setText(tabelOrderan.getValueAt(baris , 3).toString());
        pemesanTa.setText(tabelOrderan.getValueAt(baris , 4).toString());
        tambahBtn.setEnabled(false);
        jComboBoxItem.setEnabled(false);
        banyakTf.setEditable(false);
        pemesanTa.setEditable(true);
    }//GEN-LAST:event_tabelOrderanMouseClicked

